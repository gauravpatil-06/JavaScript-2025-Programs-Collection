//Assignment Operators
a = 20;
b = 5;

c = a;
console.log("Value of variable a is assigned to variable c (c = a) = ", c);

a += b;
console.log("Addition (a -= b) = ", a);

a -= b;
console.log("Substration (a -= b) = ", a);

a *= b;
console.log("Multiplication (a *= b) = ", a);

a /= b;
console.log("Division (a /= b) = ", a);

a %= b;
console.log("Modulus (a %= b) = ", a);

a **= b;;
console.log("Exponential (a **= b) = ", a); //20*20*20*20*20 = 3200000