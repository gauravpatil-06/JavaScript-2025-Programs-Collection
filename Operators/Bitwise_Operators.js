//Bitwise Operators
a = 12;
b = 13;

c = a & b;
console.log("Bitwise AND (a & b) = ", c);

c = a | b;
console.log("Bitwise OR (a | b) = ", c);

c = a ^ b;
console.log("Bitwise XOR (a ^ b) = ", c);

c =  ~12;
console.log("Bitwise NOT (~12) = ", c);

c = 14 << 2;
console.log("Bitwise Left Shift (14 << 2) = ", c);

c = 14 >> 2;
console.log("Bitwise Right Shift (14 >> 2) = ", c);