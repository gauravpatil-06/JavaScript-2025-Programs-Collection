//AutoIncrement(++) Operators
a = 20;
console.log("Value of a before Incrementation = ", a);
console.log("Value of a after Pre - Incrementation = ", ++a);
console.log("Value of a after Post - Incrementation = ", a++);

//AutoDecrement(--) Operators
console.log("\nValue of a before Decrementation = ", a);
console.log("Value of a after Pre - Decrementation = ", --a);
console.log("Value of a after Post - Decrementation = ", a--);