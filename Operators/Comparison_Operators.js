//Comparison Operators
a = 20;
b = 5;

c = a < b;
console.log("Less than (a < b) = ", c);

c = a > b;
console.log("Greater than (a > b) = ", c);

c = a <= b;
console.log("Less than or Equal (a <= b) = ", c);

c = a >= b;
console.log("Greater than or Equal (a >= b) = ", c);

c = a == b;
console.log("Equal (a == b) = ", c);

c = a != b;
console.log("Is not Equal (a != b) = ", c);

c = a === b;
console.log("Identical (a === b) ", c);

c = a !== b;
console.log("Not Identical (a !== b) ", c);