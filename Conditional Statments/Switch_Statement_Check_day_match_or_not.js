//Switch Statement : Check day which is match or not 
const day = "Monday";

switch(day)
{
	case "Monday"	 : console.log("It is Monday");
	break;
	case "Tuesday"	 :  console.log("It is Tuesday");
	break;
	case "Wednessday":  console.log("It is Wednessday");
	break;
	case "Thursday"  :  console.log("It is Thursday");
	break;
	case "Friday"	 :  console.log("It is Friday");
	break;
	case "Saturday"	 :  console.log("It is Saturday");
	break;
	case "Sunday"	 :  console.log("It is Sunday");
	break;		
	default : console.log("Invalied Day!!!");
}