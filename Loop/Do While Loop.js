//Do While Loop
//Start with 0
let i = 0;
do
{
	console.log("Value of i = ",i);
    i++;
}while (i < 5);

console.log("\n");

//Start with 1
let x = 1;
do
{
	console.log("Value of x = ",x);
    x++;
}while (x <= 5);

